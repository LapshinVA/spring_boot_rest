package ru.edu.springauth.controller;

import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ExceptionHandler;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;
import ru.edu.springauth.exception.UnauthorizedUser;
import ru.edu.springauth.model.Authorities;
import ru.edu.springauth.service.AuthorizationService;

import java.util.List;

@RestController
public class AuthorizationController {
    AuthorizationService service;

    public AuthorizationController(AuthorizationService service) {
        this.service = service;
    }

    @GetMapping("/authorize")
    public List<Authorities> getAuthorities(@RequestParam("user") String user, @RequestParam("password") String password) {
        return service.getAuthorities(user, password);
    }

    @ExceptionHandler(UnauthorizedUser.class)
    public ResponseEntity<String> handler(UnauthorizedUser e) {
        ResponseEntity<String> response = new ResponseEntity<>(e.getMessage(), HttpStatus.UNAUTHORIZED);
        System.out.println(e.getMessage());
        return response;
    }
}